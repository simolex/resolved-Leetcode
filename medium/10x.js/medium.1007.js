/**
 * 1007. Minimum Domino Rotations For Equal Row
 *
 * @param {number[]} tops
 * @param {number[]} bottoms
 * @return {number}
 */
var minDominoRotations = function (tops, bottoms) {
    let fail = Infinity;
    let n = tops.length;
    if (n !== bottoms.length) return -1;

    const countRotation = (nowTops, nowBottoms) => {
        let countByTop = 0;
        let firstTopNumber = nowTops[0];
        for (let i = 1; i < n; i++) {
            if (nowTops[i] !== firstTopNumber && nowBottoms[i] === firstTopNumber) {
                countByTop++;
            } else if (nowTops[i] !== firstTopNumber && nowBottoms[i] !== firstTopNumber) {
                return fail;
            }
        }
        let countByBottom = 1;
        for (let i = 1; i < n; i++) {
            if (nowBottoms[i] !== firstTopNumber && nowTops[i] === firstTopNumber) {
                countByBottom++;
            }
        }
        return Math.min(countByTop, countByBottom);
    };

    const minRotation = Math.min(countRotation(tops, bottoms), countRotation(bottoms, tops));
    return minRotation !== fail ? minRotation : -1;
};

